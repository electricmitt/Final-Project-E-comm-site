const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const SignUps = new Schema({
    fullName: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

const SignUp = mongoose.model("members", SignUps);

module.exports = SignUp;